	// 1. –î–æ–±–∞–≤–∏–ª–∏ TRIAL_BALANCE_AMOUNT –Ω–∞ –±–∞–ª–∞–Ω—Å (50‚ÇΩ)
	// 2. –°–æ–∑–¥–∞–µ–º –∫–æ–Ω—Ñ–∏–≥ –ë–ï–ó —Å–ø–∏—Å–∞–Ω–∏—è –¥–µ–Ω–µ–≥
	// 3. –ê–≤—Ç–æ—Å–ø–∏—Å–∞–Ω–∏–µ –±—É–¥–µ—Ç —Å–ø–∏—Å—ã–≤–∞—Ç—å –ø–æ PRICE_PER_DAY (1‚ÇΩ) –≤ –¥–µ–Ω—å
